CREATE TABLE IF NOT EXISTS FACT_FLAT
(
REALESTATE_ID INTEGER PRIMARY KEY,
AGENCY_ID INTEGER,
LOCALITY_ID INTEGER,
FLAT_TYPE_ID INTEGER,
FLAT_FACILITY_ID INTEGER,
FLAT_SIZE REAL,
TOTAL_ROOMS REAL,
TOTAL_LIVING_ROOMS REAL,
TOTAL_BEDROOMS REAL,
TOTAL_BATHROOMS REAL,
IsActive INTEGER,
CREATION_DATE TEXT,
MODIFIED_DATE TEXT,
HasPictures INTEGER,
COLD_PRICE REAL,
WARM_PRICE REAL,
ADDITIONAL_COSTS REAL,
PARKING_SPACE_PRICE REAL,
foreign key (AGENCY_ID) references DIM_AGENCY(AGENCY_ID),
foreign key (LOCALITY_ID) references DIM_LOCALITY(LOCALITY_ID),
foreign key (FLAT_TYPE_ID) references DIM_FLAT_TYPE(FLAT_TYPE_ID),
foreign key (FLAT_FACILITY_ID) references DIM_FLAT_FACILTIY(FLAT_FACILITY_ID)
);